// ===================================================================================================
// IMPORTS
// ===================================================================================================
var express = require('express');

// ===================================================================================================
// INICIALIZACIONES
// ===================================================================================================
var router = express();

// ===================================================================================================
// METODOS -- GET
// ===================================================================================================
router.get('/', (req, res) => {
  res.status(200).json({
    ok: true,
    mensaje: 'Peticion realizada correctamente'
  });
});

// ===================================================================================================
// EXPORT
// ===================================================================================================
module.exports = router;
